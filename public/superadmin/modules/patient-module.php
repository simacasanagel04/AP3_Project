<script>
function validatePatientForm(f){
    if(f.pat_email && f.pat_email.value && !/^[^@]+@[^@]+\.[a-z]+$/i.test(f.pat_email.value)){
        alert("Invalid email format");
        return false;
    }
    if(f.pat_contact_num && !/^09\d{9}$/.test(f.pat_contact_num.value)){
        alert("Invalid phone number format. Use 09xxxxxxxxx");
        return false;
    }
    return true;
}

function copyPasswordPatient() {
    const passwordInput = document.getElementById('tempPasswordPatient');
    if (passwordInput) {
        passwordInput.select();
        document.execCommand('copy');
        alert('Password copied to clipboard!');
    }
}

